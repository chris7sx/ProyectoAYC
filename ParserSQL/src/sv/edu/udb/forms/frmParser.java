    private void crearNodos(DefaultMutableTreeNode raiz) {
		ArrayList<String> bases = new ArrayList<String>();
		
		try {
			Connection con = Conexion.getMainConnection(); 					
			DatabaseMetaData meta = con.getMetaData();
			ResultSet res = meta.getCatalogs();
			//ResultSet res = meta.getTables(null, null, "%", new String[] {"TABLE"});
            while (res.next()) {
               bases.add(res.getString("TABLE_CAT"));
               //bases.add(res.getString("TABLE_NAME") + "-" + res.getString("TABLE_CAT") + "-" + res.getString(3));
            }
            res.close();
            con.close();
		} catch (SQLException e){
			JOptionPane.showMessageDialog(null, e);
		}
		
		
	    DefaultMutableTreeNode bdd = null;
	    DefaultMutableTreeNode tabla = null;
	    
	    for(String base : bases){
	    	bdd = new DefaultMutableTreeNode(base);
		    raiz.add(bdd);
	    }
	}
    
