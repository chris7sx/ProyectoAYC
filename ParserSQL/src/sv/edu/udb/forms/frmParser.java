    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String entrada = txtEntrada.getText();
        InputStream is = new ByteArrayInputStream(entrada.getBytes());

        if(parser == null){
            parser = new SQLparser(is);
        } else {
            SQLparser.ReInit(is);
        }

        try{

            switch (SQLparser.inicio()){
                case 0 :
                txtSalida.setForeground(Color.black);
                txtSalida.setText("Sentencias correctas");
                jButton2.setEnabled(true);
                break;
            }
        } catch (Exception e) {
            txtSalida.setForeground(Color.red);
            String error = "";
            if(e.getMessage().toString().contains("Was expecting:")){
                error = e.getMessage().toString().replace("Was expecting:", "Se esperaba:").replace("<EOF>", "");
                jButton2.setEnabled(false);
            } else {
                error = e.getMessage().toString().replace("Was expecting one of:", "Se esperaba uno de los siguientes:").replace("<EOF>", "");
                jButton2.setEnabled(false);
            }
            txtSalida.setText("Error en la sentencia.\n" + error);
            jButton2.setEnabled(false);
        } catch (Error e) {
            txtSalida.setForeground(Color.red);
            String error = e.getMessage().toString().replace("Was expecting one of:", "Se esperaba:").replace("<EOF>", "");
            error.replace("Was expecting one of:", "Se esperaba:");
            txtSalida.setText("Error en la sentencia.\n" + error);
            jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void dbTreeValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_dbTreeValueChanged
        String db = evt.getPaths()[0].getLastPathComponent().toString();
        lblTablas.setText(db);
        if(!db.equals("SQL Server")){
            try {
                Connection con = Conexion.getMainConnection(); 					
                con.setCatalog(db);
                Statement stmt = con.createStatement();
                ResultSet res = stmt.executeQuery("SELECT TABLE_NAME FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_TYPE = 'BASE TABLE' AND TABLE_CATALOG='" + db + "'");
                String salida = "";
                while (res.next()) {
                   salida += "⁃ " + res.getString("TABLE_NAME") + "\n";
                }
                res.close();
                con.close();
                
                txtTablas.setText(salida);
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_dbTreeValueChanged

